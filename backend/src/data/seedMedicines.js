function formatDose(value) {
  return String(value).replace(/\.0$/, "");
}

const extendedSeedMedicines = extendedMedicineTemplates.flatMap(
  ([genericName, variants, allergyTags, notes]) =>
    variants.map(([value, dosageUnit, form]) => ({
      name: `${genericName} ${formatDose(value)} ${dosageUnit} ${form}`,
      genericName,
      minDosageValue: value,
      maxDosageValue: value,
      dosageUnit,
      allergyTags,
      notes
    }))
);

const pakistanPopularSeedMedicines = pakistanPopularMedicineTemplates.flatMap(
  ([brandName, genericName, variants, allergyTags, notes]) =>
    variants.map(([value, dosageUnit, form]) => ({
      name: `${brandName} ${formatDose(value)} ${dosageUnit} ${form}`,
      genericName,
      minDosageValue: Number.isFinite(Number(value)) ? Number(value) : null,
      maxDosageValue: Number.isFinite(Number(value)) ? Number(value) : null,
      dosageUnit,
      allergyTags,
      notes
    }))
);

